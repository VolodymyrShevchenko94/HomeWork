package Ex14x04;

public class Ex7 {
    public static void main(String[] args) {
        final int constantNumber = 7;
        final int power_of_number = 10;
        for (int i = 1; i <= power_of_number; i++) {
            System.out.println(constantNumber + " * " + i + " = " + constantNumber * i);
        }
    }
}
