package Ex14x04;

public class Ex7_2 {
    public static void main(String[] args) {
        int power_number = 10;
        int a = 7;
        int summ = 0;
        for (int i = 1; i <= power_number ; i++) {
            summ = a * i;
            System.out.println(a + "*" + i +  " = " + summ);



        }

    }
}
